        document.addEventListener('DOMContentLoaded', function() {
            const wordContainer = document.querySelector('.word-container');
            const words = wordContainer.getAttribute('data-words').split('|');
            let currentIndex = 0;
            let pointerElement;
            let writeInterval, eraseInterval;

            function writeWord(word) {
                let i = 0;
                writeInterval = setInterval(() => {
                    wordContainer.textContent += word[i];
                    pointerElement = document.createElement('span');
                    pointerElement.classList.add('pointer');
                    wordContainer.appendChild(pointerElement);
                    i++;
                    if (i >= word.length) {
                        clearInterval(writeInterval);
                        eraseWord();
                    }
                }, 500);
            }

            function eraseWord() {
                eraseInterval = setInterval(() => {
                    if (wordContainer.textContent.length > 0) {
                        wordContainer.textContent = wordContainer.textContent.slice(0, -2);
                    } else {
                        clearInterval(eraseInterval);
                        currentIndex = (currentIndex + 1) % words.length;
                        showNextWord();
                    }
                }, 300);
            }

            function showNextWord() {
                const currentWord = words[currentIndex];
                writeWord(currentWord);
            }

            showNextWord();
        });
